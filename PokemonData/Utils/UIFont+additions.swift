    class var demiBold: UIFont {
        return UIFont(name: "AvenirNext-DemiBold", size: 17.5)!
    }
    
    class var medium: UIFont {
        return UIFont(name: "AvenirNext-Medium", size: 16.0)!
    }
    
    class var bold: UIFont {
        return UIFont(name: "AvenirNext-Bold", size: 25.0)!
    }
    
}
